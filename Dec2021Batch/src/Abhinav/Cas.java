package Abhinav;

public class Cas {
	
	public static void main(String[] args) {          // Explicit casting 
		
		double a =43.75;          // memory size is 8 byte 
		System.out.println(a);
		
		int b = (int)a;            // memory size is 4 byte        // casting operator
		System.out.println(b);
		
		byte c =(byte)b;         // memory size is 2 byte
		System.out.println(c);
		
		
	}

}
